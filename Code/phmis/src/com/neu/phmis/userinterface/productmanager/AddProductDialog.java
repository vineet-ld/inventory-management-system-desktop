    private void createPersonBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createPersonBtnActionPerformed

        try {
            String errors = validateForm();

            if (errors.isEmpty()) {

                Product product = productOrganization.getProductDirectory()
                        .addProduct();
                product.setProductName(productNameTxt.getText().trim());
                BigDecimal cost = BigDecimal.valueOf(Double
                        .parseDouble(productCostTxt.getText().trim()));
                product.setProductCost(cost.setScale(2, RoundingMode.CEILING));
                product.setProductDescription(productDescTxt.getText().trim());

                this.setVisible(false);
                manageProductsPanel.populateTable();

                CommonUtil.saveChanges(ecosystem);

            } else {
                WebOptionPane.showMessageDialog(null, errors, "Errors",
                        WebOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            WebOptionPane.showMessageDialog(null,
                    "Some Unexpected Error Occured\n" + e.getMessage(), "Error",
                    WebOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_createPersonBtnActionPerformed

