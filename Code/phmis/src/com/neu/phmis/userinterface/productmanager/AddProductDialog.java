    /* Validates form
    
     Parameters: NONE
    
     Returns:
     1. errorText - STRING errors if there are any
     2. errorText - STRING blank if there are no errors
     */
    private String validateForm() {

        String errorText = "";

        try {

            int errorCount = 1;

            String productName = productNameTxt.getText().trim();
            String productCost = productCostTxt.getText().trim();
            String productDescription = productDescTxt.getText().trim();

            if (productName.equals("")) {
                errorText += errorCount + ". Please enter the Product Name\n";
                errorCount++;
            }

            if (productCost.equals("")) {
                errorText += errorCount + ". Please enter the Product Cost\n";
                errorCount++;
            } else if (CommonUtil.isNAN(productCost)) {
                errorText += errorCount + ". Invalid Product Cost\n";
                errorCount++;
            } else if (Double.parseDouble(productCost) <= 0.0) {
                errorText += errorCount + ". Invalid Product Cost\n";
                errorCount++;
            }

            if (productDescription.equals("")) {
                errorText += errorCount + ". Please enter the Product "
                        + "Description\n";
                errorCount++;
            }

        } catch (Exception e) {
            WebOptionPane.showMessageDialog(null,
                    "Some Unexpected Error Occured\n" + e.getMessage(), "Error",
                    WebOptionPane.ERROR_MESSAGE);
        } finally {
            return errorText;
        }

    }

