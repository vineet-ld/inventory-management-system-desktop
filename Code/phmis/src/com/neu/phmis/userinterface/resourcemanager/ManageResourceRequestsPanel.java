    /*
     Populate requests in the tables
    
     Parameters: NONE
    
     Returns: NONE
     */
    public void populateTables() {

        try {

            DefaultTableModel defaultTableModel
                    = (DefaultTableModel) surgeryRequestTbl.getModel();
            defaultTableModel.setRowCount(0);

            SurgeryRequest surgeryRequest = null;
            Object row[];
            for (Request request : hospitalEnterprise.getSurgeryRequestQueue()
                    .getRequestList()) {

                surgeryRequest = (SurgeryRequest) request;
                row = new Object[5];
                row[0] = surgeryRequest;
                row[1] = surgeryRequest.getSurgicalOperation().getDoctor();
                row[2] = CommonUtil.changeDateFormat(surgeryRequest
                        .getCreatedOn(), "dd MMM, yyyy");
                row[3] = CommonUtil.changeDateFormat(surgeryRequest
                        .getStartDate(), "dd MMM, yyyy") + " - " + CommonUtil
                        .changeDateFormat(surgeryRequest.getEndDate(), "dd "
                                + "MMM, yyyy");
                row[4] = surgeryRequest.getStatus();
                defaultTableModel.addRow(row);

            }

            defaultTableModel = (DefaultTableModel) deviceRequestTbl.getModel();
            defaultTableModel.setRowCount(0);

            DeviceRequest deviceRequest = null;
            for (Request request : resourceManager.getDeviceRequestQueue()
                    .getRequestList()) {

                deviceRequest = (DeviceRequest) request;
                row = new Object[3];
                row[0] = deviceRequest;
                row[1] = CommonUtil.changeDateFormat(deviceRequest
                        .getCreatedOn(), "dd MMM, yyyy");
                row[2] = deviceRequest.getStatus();
                defaultTableModel.addRow(row);

            }

        } catch (Exception e) {
            WebOptionPane.showMessageDialog(null,
                    "Some Unexpected Error Occured\n" + e.getMessage(), "Error",
                    WebOptionPane.ERROR_MESSAGE);
        }

    }

