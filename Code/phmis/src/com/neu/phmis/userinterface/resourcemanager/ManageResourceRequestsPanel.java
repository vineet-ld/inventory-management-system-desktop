    private void viewRequestBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewRequestBtnActionPerformed

        try {

            int selectedRow = surgeryRequestTbl.getSelectedRow();

            if (selectedRow >= 0) {

                SurgeryRequest surgeryRequest
                        = (SurgeryRequest) surgeryRequestTbl
                        .getValueAt(selectedRow, 0);
                ViewScheduleRequestDialog viewScheduleRequestDialog
                        = new ViewScheduleRequestDialog(MainFrame.mainFrame,
                                true, surgeryRequest, resourceManager);
                viewScheduleRequestDialog.setVisible(true);

            } else {
                WebOptionPane.showMessageDialog(null,
                        "No Request Selected", "Warning",
                        WebOptionPane.WARNING_MESSAGE);
            }

        } catch (Exception e) {
            WebOptionPane.showMessageDialog(null,
                    "Some Unexpected Error Occured\n" + e.getMessage(), "Error",
                    WebOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_viewRequestBtnActionPerformed

