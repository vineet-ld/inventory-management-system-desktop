    private void logoutBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_logoutBtnActionPerformed

        try {
            CommonUtil.saveChanges(ecosystem);
            LoginPanel loginPanel
                    = new LoginPanel(MainFrame.mainFrame.getContainer(),
                            ecosystem);
            CommonUtil.insertNewPanel(MainFrame.mainFrame.getContainer(),
                    "Login Panel", loginPanel, this);
        } catch (Exception e) {
            WebOptionPane.showMessageDialog(null,
                    "Some Unexpected Error Occured" + e.getMessage(), "Error",
                    WebOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_logoutBtnActionPerformed

