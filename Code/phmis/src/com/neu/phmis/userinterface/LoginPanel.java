    private void loginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBtnActionPerformed

        try {

            String userName = userNameText.getText().trim();
            String password = String.valueOf(passwordText.getPassword());

            if (userName.isEmpty() || password.isEmpty()) {
                WebOptionPane.showMessageDialog(null,
                        "Please enter user name and/or password", "Error",
                        WebOptionPane.ERROR_MESSAGE);
            } else {

                Person person = ecosystem.getMasterPersonDirectory()
                        .authenticate(userName, password);

                if (person != null) {

                    CommonUtil.insertNewPanel(container, "Dashboard",
                            person.getUserAccount().getRole()
                            .getDashboard(person), this);

                    if (person.getUserAccount().getStatus() == 0) {

                        SetPasswordDialog setPasswordDialog
                                = new SetPasswordDialog(MainFrame.mainFrame,
                                        true, person, ecosystem, this);
                        setPasswordDialog.setVisible(true);

                    }

                } else {
                    WebOptionPane.showMessageDialog(null,
                            "Invalid Username and/or password",
                            "Authentication failed",
                            WebOptionPane.WARNING_MESSAGE);
                }

            }

        } catch (Exception e) {
            WebOptionPane.showMessageDialog(null,
                    "Some Unexpected Error Occured\n" + e.getMessage(), "Error",
                    WebOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_loginBtnActionPerformed


